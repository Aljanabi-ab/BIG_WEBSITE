<h3>Flex Box</h3>
<p>
A flex box provides a one dimensional layout of different elements. The flexbox container eases the arrangement of elements within the container by providing attributes (helen) such as equal width spanning, optimized spacing between elements, optimization of row and column arrangements with ease. It is important to have a structured layout format for web pages. This constraints objects within a defined layout that is symmetric and appealing to the eye. It also enables separation of different content blocks that contain different bits of information making it easy for the viewer to skeem through content easily and access Information they need. The user experience is thus critically improved
</p>
<p>
When working with Flex- Boxes the first step is to define the flex direction, which defines the type of layout whether it is row based or column based. Typically, there are mainly four flex-direction attributes, which are row, row-reverse, column, column-reverse. After defining the flex-direction attribute in CSS the next step is to identify the start and end lines (Collins). The start and end lines are where content begins to be placed on the flex-box. If row or column direction is selected content begins from the right and top respectively, if a hyphenation is added followed by reverse for either row or column, content placement begins from the right and bottom respectively.
</p>
<p>
The next step which is critical is to define the container size, which is achieved by declaring the display attribute of the area of concern as flex. Additional properties applied to flex properties include
</p>
<ul>
<li>flex-grow</li>
<li>flex-shrink</li>
<li>flex-basis</li>
<li>flex flow</li>
</ul>
<p>
Flex-grow stretches the elements within the flex container to grow and stretch through the entire size of the defined container. Flex -shrink, minimizes the width of elements within the container to fit to the width of the container. Flex basis creates customizable width of the elements within the container. Whereas flex flow changes the property of items within the container to have additional features such as wrapping and overflow of content to a different line based on the defined axis of the flex container.
</p>
<p>
There are different layout modes that can be utilized to achieve the same effect on content. Some developers prefer using a grid which has different spanning optimization capabilities too (Borgen).
</p>
