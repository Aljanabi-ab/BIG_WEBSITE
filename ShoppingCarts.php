<h3>Monetizing Websites</h3>
<p>
There are several ways to monetize websites based on the purpose and use of the website. Some websites make money directly through selling different goods and services(Sirkkunen). The implementation involves having different products on the website, with a shopping cart that the user can utilize to select items for purchase. In addition, the website includes integration of different payment platforms that facilitate funds transfer and payment processing. The website owner then processes the order and arranges for delivery of the product or service
</p>
<p>
Another option for monetizing a website is through the use of ads such as google ads. The website owner creates different containers for ad elements within the website that are optimized by google(Turner). The user is then paid based on per click rates to the websites. Different pay per click strategies increase payment based on the user activity on the advertised site if the user proceeded to register on the platform or even purchase on the platform.
</p>
<p>
Some websites also tap on marketing initiatives to the captured audience to the website. This strategy is similar to that of influencers whereby the website owner presents web analytics to the advertising agency or client, upon which they provide a rate card for advertisement on their site(Grubenmann). The rates are based on the form of advertisement. Some advertisements are just based on mention which include a link to where a service or product is offered while others include the entire content development package which may include a review of content through articles, or media content.
</p>
<p>
ALternatively some websites operate based on donations. This is typical for several open source softwares developed for the public to improve on software development. Upon download of the software needed the downloader is given an option to donate to the company in order to cover their operation costs.
</p>

